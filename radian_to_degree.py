"""
Radian to Degree
Create a function that takes an angle in radians and converts it into degrees.
Examples
to_degree(math.pi) ➞ 180

to_degree(math.pi/2) ➞ 90

to_degree(math.pi/4) ➞ 45
"""

import math
def to_degree(radian):
    pi=math.pi
    degree=(radian*180)/pi
    return (degree)
to_degree(math.pi)


to_degree(math.pi/4)
